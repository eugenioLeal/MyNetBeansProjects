int busquedaSecuencial(int* E, int n, int k) {
    int respuesta, indice;
    respuesta = -1;
    for(int i = 0; i < n; i++)
        if (k == E[indice]) {
            respuesta = indice;
            break;
        }
    return respuesta;
    
    
}

